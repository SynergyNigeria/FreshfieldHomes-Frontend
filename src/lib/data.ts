export function formatPrice(price: number | string): string {
  const numericPrice = typeof price === "number" ? price : Number(price);
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0,
  }).format(numericPrice);
}

